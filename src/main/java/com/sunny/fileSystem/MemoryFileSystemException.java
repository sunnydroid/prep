package com.sunny.fileSystem;

public class MemoryFileSystemException extends Exception {

	private static final long serialVersionUID = 1L;

	public MemoryFileSystemException() {
	}

	public MemoryFileSystemException(String arg0) {
		super(arg0);
	}

	public MemoryFileSystemException(Throwable arg0) {
		super(arg0);
	}

	public MemoryFileSystemException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
